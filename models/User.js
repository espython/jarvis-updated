import mongoose from 'mongoose';

/**
 * Initialize the user schema
 */
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
  avatar: {
    type: String,
  },
});

const User = mongoose.model('users', userSchema);

export default User;
